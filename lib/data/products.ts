// Helper functions
export const getProductBySlug = (slug: string): Product | undefined => {
  return products.find((p) => p.slug === slug);
};

export const getProductById = (id: string): Product | undefined => {
  return products.find((p) => p.id === id);
};

export const getFeaturedProducts = (): Product[] => {
  return products.filter((p) => p.badge === 'bestseller' || p.badge === 'vip').slice(0, 8);
};

export const getHotProducts = (): Product[] => {
  return products.filter((p) => p.badge === 'hot' || p.badge === 'new').slice(0, 8);
};

export const getTopSellers = (): Product[] => {
  return [...products].sort((a, b) => b.reviewCount - a.reviewCount).slice(0, 8);
};

export const getProductsByBrand = (brand: string): Product[] => {
  return products.filter((p) => p.brand.toLowerCase() === brand.toLowerCase());
};

export const getRelatedProducts = (productId: string, limit: number = 4): Product[] => {
  const product = getProductById(productId);
  if (!product) return [];
  
  return products
    .filter((p) => p.id !== productId && p.brand === product.brand)
    .slice(0, limit);
};

export const getAllBrands = (): string[] => {
  return [...new Set(products.map((p) => p.brand))];
};

export const getAllStorageOptions = (): string[] => {
  const storageSet = new Set<string>();
  products.forEach((p) => p.storageVariants.forEach((s) => storageSet.add(s.trim())));
  return Array.from(storageSet).sort((a, b) => {
    const aNum = parseInt(a);
    const bNum = parseInt(b);
    return aNum - bNum;
  });
};

export const getAllRamOptions = (): string[] => {
  const ramSet = new Set<string>();
  products.forEach((p) => {
    const rams = p.specifications.ram.split('/').map((r) => r.trim());
    rams.forEach((r) => ramSet.add(r));
  });
  return Array.from(ramSet).sort((a, b) => {
    const aNum = parseInt(a);
    const bNum = parseInt(b);
    return aNum - bNum;
  });
};
